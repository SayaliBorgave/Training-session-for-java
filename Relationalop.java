package sayali;

public class Relationalop {

	public static void main(String[] args) {
		int a = 11;
		int b = 12;
		System.out.println("a==b =" +(a==b));
		System.out.println("a!=b =" +(a!=b));
		System.out.println("a>b =" +(a>b));
		System.out.println("a<b =" +(a<b));
		System.out.println("a<=b =" +(a<=b));
		System.out.println("a>=b =" +(a>=b));
		
	}

}
